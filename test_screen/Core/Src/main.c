int decider(float tem, float hum){
	float tem_low = (65-32)/1.8;
	float tem_high = (75-32) / 1.8;
	float hum_low = 1000;

	if (tem<tem_low)
		return 2;

	if (hum<hum_low)
		return 4;

	if (tem>tem_high)
		return 1;

	return 0;

}

